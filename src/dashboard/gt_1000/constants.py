DEVICE_ID_BCAST = [0x7F]
MODEL_ID = [0x00, 0x00, 0x00, 0x4F]
RQ1_COMMAND_ID = [0x11]
DT1_COMMAND_ID = [0x12]
DT1_SYSEX_HEADER = MANUFACTURER_ID + DEVICE_ID_BCAST + MODEL_ID + DT1_COMMAND_ID
RQ1_SYSEX_HEADER = MANUFACTURER_ID + DEVICE_ID_BCAST + MODEL_ID + RQ1_COMMAND_ID

SYSEX_START = [0xF0]
SYSEX_END = [0xF7]

NON_RT_MSG = [0x7E]
BCAST_DEV_ID = [0x7F]
GEN_INFO = [0x06]
IDENTITY_REQUEST = [0x01]
IDENTITY_REPLY = [0x02]

ONE_BYTE = [0x0, 0x0, 0x0, 0x1]

# [0xf0, 0x7e, 0x7f, 0x6, 0x1, 0xf7]
IDENTITY_REQUEST_MSG = (
    SYSEX_START + NON_RT_MSG + BCAST_DEV_ID + GEN_INFO + IDENTITY_REQUEST + SYSEX_END
)

